Pengertian Migration, Model dan Resource

1. Migration = Membantu mengelola database secara efisien menggunakan kode, membuat (create), mengubah(edit) dan menghapus (delete) struktur tabel dan kolom pada database dengan cepat
2. Model = Merupakan representasi dari tabel database dapat melakukan operasi database membuat, membaca mengupdate dan menghapus data
3. Resource = Memudahkan dalam mengelola API yang datanya sudah dibuat dalam bentuk JSON